    public void getPatientTendOnLine() {
        final Call<ResultBeans<PatientB>> mTendPatient = mHttp.getPatientTend("1CD342661A5241629B9AE4487725D278");
        mTendPatient.enqueue(new Callback<ResultBeans<PatientB>>() {
            @Override
            public void onResponse(Response<ResultBeans<PatientB>> response, Retrofit retrofit) {

                if (response != null && response.body() != null) {
                    mPatients.clear();
                    mPatients.addAll(response.body().getINFO());
                    mAdapter.notifyDataSetChanged();
                }

            }

            @Override
            public void onFailure(Throwable t) {
                t.printStackTrace();
            }
        });
    }

